# vertical, flexural, torsional = sensor_dict_combined["AICD 001"]

# Example function to get specific direction
def get_sensor_direction(sensor_id, direction):
    """
    Get sensor value for specific direction
    direction: 'vertical', 'flexural', or 'torsional'
    """
    directions = {'vertical': 0, 'flexural': 1, 'torsional': 2}
    return sensor_dict_combined[sensor_id][directions[direction]]

